void updateSandesh(bool flag, float maxScale)
{
    static float currentScale = 0.0f;
    static bool wasActive = false;
    float scaleIncrement = 0.02f; // Controls animation speed
    
    // Only display and animate when flag is true
    if (flag) {
        // Reset scale if this is the first time flag became true
        if (!wasActive) {
            currentScale = 0.0f;
            wasActive = true;
        }
        
        // Draw shadow (slightly larger and black)
        drawSandesh(0, 0, currentScale * 1.02, 0, 0, 0, 1.0);
        
        // Draw text with gold color
        drawSandesh(0, 0, currentScale, 0.988, 0.984, 0.69, 1.0);
        
        // Increase scale until we reach maxScale
        if (currentScale < maxScale) {
            currentScale += scaleIncrement;
            if (currentScale > maxScale) {
                currentScale = maxScale;
            }
        }
    } else {
        // Reset when flag becomes false
        wasActive = false;
        currentScale = 0.0f;
    }
}
